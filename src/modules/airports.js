// ── Lookup helpers ────────────────────────────────────────────────────
const _byIata = new Map(AIRPORTS.map((a) => [a.iata, a]));

export function getAirport(iata) {
  return _byIata.get(iata?.toUpperCase()) || null;
}

/**
 * Register an airport dynamically (e.g. discovered from a flight API).
 * Won't overwrite existing entries.
 */
export function registerAirport({ iata, name, city, country, lat, lng }) {
  if (!iata) return;
  const code = iata.toUpperCase();
  if (_byIata.has(code)) return; // already known
  const entry = { iata: code, name: name || code, city: city || "", country: country || "", lat: lat ?? 0, lng: lng ?? 0 };
  AIRPORTS.push(entry);
  _byIata.set(code, entry);
}

/**
 * Search airports by IATA code, name, or city. Returns up to `limit` results.
 */
export function searchAirports(query, limit = 8) {
  if (!query) return [];
  const q = query.toLowerCase();
  const results = [];

  // Exact IATA match first
  for (const a of AIRPORTS) {
    if (a.iata.toLowerCase() === q) {
      results.push(a);
      break;
    }
  }

  // Then prefix matches on IATA
  for (const a of AIRPORTS) {
    if (results.length >= limit) break;
    if (a.iata.toLowerCase().startsWith(q) && !results.includes(a)) {
      results.push(a);
    }
  }

  // Then fuzzy matches on city/name
  for (const a of AIRPORTS) {
    if (results.length >= limit) break;
    if (
      !results.includes(a) &&
      (a.city.toLowerCase().includes(q) ||
        a.name.toLowerCase().includes(q) ||
        a.country.toLowerCase() === q)
    ) {
      results.push(a);
    }
  }

  return results.slice(0, limit);
}
